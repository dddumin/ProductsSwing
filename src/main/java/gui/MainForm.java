    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(this.prodList.getCount() == 0){
            JOptionPane.showMessageDialog(rootPane, "No registered products", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(this.transaction.isFull()){
            JOptionPane.showMessageDialog(rootPane, "Cart is full", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        new BuyProducts(this.prodList, this.transaction).setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(this.transaction.getCount() == 0){
            JOptionPane.showMessageDialog(rootPane, "Cart is empty", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }        
        new RemoveProductsForm(this.transaction).setVisible(true);       
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(this.transaction.getCount() == 0){
            JOptionPane.showMessageDialog(rootPane, "Cart is empty", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double cost = this.transaction.getTotalCost();
        int n = this.transaction.getCount();
        for (int i = n; i > 0; i--) {
            this.prodList.setQuantity(this.transaction.get(i - 1));
            this.transaction.deleteProduct(i - 1);
        }
        JOptionPane.showMessageDialog(rootPane, "Total cost: " + cost, "Result", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton5ActionPerformed

    

