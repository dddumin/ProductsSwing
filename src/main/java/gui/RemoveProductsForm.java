    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.transaction.deleteProduct(jComboBox1.getSelectedIndex());
        Product[] products = new Product[this.transaction.getCount()];
        for (int i = 0; i < this.transaction.getCount(); i++) {
            products[i] = this.transaction.get(i);
        }
        jComboBox1.setModel(new DefaultComboBoxModel<>(products)); 
        if(this.transaction.getCount() == 0){
            JOptionPane.showMessageDialog(rootPane, "Cart is empty", "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

