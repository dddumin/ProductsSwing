    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.transaction.isFull()){
            JOptionPane.showMessageDialog(rootPane, "Cart is full", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        Product product = (Product) jComboBox1.getSelectedItem();
        if (product.getQtyOnHand() < this.transaction.countProduct(product)) {
            JOptionPane.showMessageDialog(rootPane, "Product out of stock", "Error", JOptionPane.ERROR_MESSAGE);               
            return;
        } 
        this.transaction.addProduct(product);                                
        JOptionPane.showMessageDialog(rootPane, "Product added to cart", "Result", JOptionPane.INFORMATION_MESSAGE);                   
        jList1.setModel(new DefaultComboBoxModel<>(this.transaction.getItems()));     
    }//GEN-LAST:event_jButton1ActionPerformed


